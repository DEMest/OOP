package ru.nsu.smolin.table;

/**
 * <p>Выравнивание содержимого ячейки таблицы при генерации Markdown.</p>
 *
 * <ul>
 *   <li><b>LEFT</b> — левое выравнивание. В строке разделителя используется форма <code>:---</code>.</li>
 *   <li><b>RIGHT</b> — правое выравнивание. В строке разделителя используется форма <code>---:</code>.</li>
 *   <li><b>CENTER</b> — выравнивание по центру. В строке разделителя используется форма <code>:---:</code>.</li>
 * </ul>
 */
public enum Alignment {
    LEFT, RIGHT, CENTER
}